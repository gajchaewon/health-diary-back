SELECT (SELECT MAX(comment_id) FROM comment) + ROW_NUMBER() OVER (ORDER BY RAND()) AS comment_id,
       DATE_ADD(p.created_at, INTERVAL FLOOR(RAND() * 30) DAY)                     AS created_at,
       p.diary_id,
       NOW()                                                                       AS modified_at,
       u.user_id,
       CONCAT('Comment ', ROW_NUMBER() OVER (ORDER BY RAND()))                     AS content
FROM personal_exercise_diary p
         CROSS JOIN
     user_account u
WHERE p.created_at <= NOW()
ORDER BY RAND()
LIMIT 60;

